package com.example.androidde;

public class Revenue {
	private int id;	
	private String income_source;
	private String income_num;
	private String remarks;
	private String date;
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the income_source
	 */
	public String getIncome_source() {
		return income_source;
	}
	/**
	 * @param income_source the income_source to set
	 */
	public void setIncome_source(String income_source) {
		this.income_source = income_source;
	}
	/**
	 * @return the income_num
	 */
	public String getIncome_num() {
		return income_num;
	}
	/**
	 * @param income_num the income_num to set
	 */
	public void setIncome_num(String income_num) {
		this.income_num = income_num;
	}
	/**
	 * @return the remarks
	 */
	public String getRemarks() {
		return remarks;
	}
	/**
	 * @param remarks the remarks to set
	 */
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
}
